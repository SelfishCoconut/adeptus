@pytest.fixture
def test_mcp_config(tmp_path: Path) -> Path:
    """Write a test-local mcp.yaml that points to the real server.py on the host.

    Uses sys.executable as the interpreter so the correct venv Python runs the
    server (important when the tests run inside a virtualenv).  The path to
    server.py is resolved absolutely from the repo root so the test is
    cwd-independent.
    """
    if not _SHELL_EXEC_SERVER.exists():
        pytest.skip(f"shell-exec server.py not found at {_SHELL_EXEC_SERVER}")

    config_content = f"""\
# Test-only MCP config generated by test_mcp_integration.py.
# Uses sys.executable + absolute path so the test runs correctly on the host
# without the container paths (/mcp-servers/...) that the compose stack uses.
servers:
  - name: shell-exec
    command: {sys.executable}
    args:
      - {_SHELL_EXEC_SERVER}
    tools:
      - name: run_command
        weight: light
        capability_flags:
          - shell-exec
          - filesystem-write
"""
    config_path = tmp_path / "mcp_test.yaml"
    config_path.write_text(config_content)
    return config_path


@pytest_asyncio.fixture
async def pg_schema_factory(
    monkeypatch: pytest.MonkeyPatch,
) -> AsyncGenerator[async_sessionmaker[AsyncSession], None]:
    """Session factory scoped to a throwaway Postgres schema.

    Mirrors the pattern used by auth/tests/test_integration.py and
    engagements/tests/test_integration.py.  Skips if Postgres is not reachable.
    """
    # Point settings at the integration DB for this test.
    monkeypatch.setenv("DATABASE_URL", _dsn())
    monkeypatch.setenv("ADEPTUS_ADMIN_USER", "admin_it")
    monkeypatch.setenv(
        "ADEPTUS_ADMIN_PASSWORD_HASH",
        "$argon2id$v=19$m=65536,t=3,p=4$dGVzdHNhbHQ$hashhashhashhashhashhashhashhashhashhashhas",
    )
    get_settings.cache_clear()
    get_engine.cache_clear()
    get_sessionmaker.cache_clear()

    schema = f"mcp_it_{uuid.uuid4().hex[:12]}"
    admin_engine = create_async_engine(_dsn(), isolation_level="AUTOCOMMIT")
    try:
        async with admin_engine.connect() as conn:
            await conn.execute(text(f'CREATE SCHEMA "{schema}"'))
    except Exception as exc:  # noqa: BLE001 — any failure means Postgres not available
        await admin_engine.dispose()
        pytest.skip(f"Postgres not available for integration tests: {exc}")

    engine = create_async_engine(
        _dsn(),
        connect_args={"server_settings": {"search_path": schema}},
    )
    async with engine.begin() as conn:
        await conn.run_sync(Base.metadata.create_all)

    factory = async_sessionmaker(engine, expire_on_commit=False, class_=AsyncSession)
    try:
        yield factory
    finally:
        await engine.dispose()
        async with admin_engine.connect() as conn:
            await conn.execute(text(f'DROP SCHEMA "{schema}" CASCADE'))
        await admin_engine.dispose()
        # Clear lru_cache so the next test starts fresh.
        get_settings.cache_clear()
        get_engine.cache_clear()
        get_sessionmaker.cache_clear()


def _make_mcp_app(
    factory: async_sessionmaker[AsyncSession],
    config_path: Path,
) -> FastAPI:
    """Build a minimal FastAPI app with auth + engagements + mcp routers.

    Uses a custom lifespan that:
      1. Loads the registry from the test mcp.yaml (real server.py on host).
      2. Calls subprocess_manager.startup() to spawn the real subprocess.
      3. Calls subprocess_manager.shutdown() on exit.

    get_db is overridden to use the throwaway-schema session factory so all
    DB calls from the routers (and the lifespan's admin bootstrap) land in the
    isolated test schema.
    """

    @asynccontextmanager
    async def test_lifespan(app: FastAPI) -> AsyncGenerator[None, None]:
        # Reset any stale subprocess_manager state from previous tests.
        mcp_registry._reset_registry()  # noqa: SLF001
        subprocess_manager._reset_manager()  # noqa: SLF001

        # Load the test-local registry and spawn the real shell-exec subprocess.
        mcp_registry.load_registry(config_path=str(config_path))
        await subprocess_manager.startup()

        yield

        # Terminate the subprocess on teardown.
        await subprocess_manager.shutdown()
        mcp_registry._reset_registry()  # noqa: SLF001
        subprocess_manager._reset_manager()  # noqa: SLF001

    app = FastAPI(lifespan=test_lifespan)
    register_error_handlers(app)
    app.include_router(auth_router)
    app.include_router(engagements_router)
    app.include_router(mcp_router)

    async def override_get_db() -> AsyncGenerator[AsyncSession, None]:
        async with factory() as session:
            yield session

    app.dependency_overrides[get_db] = override_get_db
    return app


