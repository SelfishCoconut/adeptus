"""Integration test for the MCP shell-exec end-to-end path.

This test proves the full round-trip:
  browser-equivalent (httpx) → FastAPI → MCP subprocess (real server.py) → FastAPI → DB

Design:
  - Uses the *real* shell-exec server.py subprocess — NOT a mock.
    subprocess_manager spawns `sys.executable <repo>/mcp-servers/shell-exec/server.py`
    via a test-local mcp.yaml written to a tmp_path, so no container paths are required
    and the test is host-runnable.

  - Spins up a *throwaway* Postgres schema (same pattern as auth/tests/test_integration.py
    and engagements/tests/test_integration.py) so the test never touches the dev/prod tables
    and cleans up on teardown.

  - Builds a minimal FastAPI app with a custom lifespan that (a) loads the registry from
    the test mcp.yaml, (b) calls subprocess_manager.startup() to spawn the real subprocess,
    and (c) calls subprocess_manager.shutdown() on exit.  Auth + engagements + mcp routers
    are all included so login, engagement creation, and tool-run all work.

  - get_db is overridden to use the throwaway-schema session factory, so the lifespan's
    admin bootstrap and all router DB calls land in the isolated schema.

  - Subprocess manager module-level state (_handles, _registry) is reset in teardown so
    successive test runs don't inherit a stale running handle.

Marked ``integration``: excluded from the default ``make test-backend`` run; use
``make test-integration`` or:
  cd backend && uv run pytest -m integration app/features/mcp/tests/test_mcp_integration.py -v

Prerequisites: Postgres reachable at the default compose DSN or ADEPTUS_TEST_DATABASE_URL.
The test skips automatically (pytest.skip) when no Postgres is available.
"""

