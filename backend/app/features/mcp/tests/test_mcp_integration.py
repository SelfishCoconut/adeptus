@pytest.mark.asyncio
async def test_tool_run_echo_hello_end_to_end(
    pg_schema_factory: async_sessionmaker[AsyncSession],
    test_mcp_config: Path,
) -> None:
    """Full round-trip: login → create engagement → POST /tool-runs → assert result + DB row.

    Steps:
      1. Seed a member user directly via the repository.
      2. Create an engagement and add the user as explicit owner (no admin bypass — §4).
      3. Log in via POST /api/v1/auth/login.
      4. POST /api/v1/tool-runs with command="echo hello".
      5. Assert HTTP 200, exit_code==0, stdout=="hello\\n".
      6. Assert a tool_runs row exists in the DB with the correct fields.
    """
    # ---- step 1: seed a member user ----------------------------------------
    async with pg_schema_factory() as session:
        member = await auth_repo.create_user(
            session, username="member_user", password_hash=_MEMBER_HASH, role="user"
        )
        await session.commit()
        member_id = member.id

    # ---- step 2: create engagement with member as owner --------------------
    async with pg_schema_factory() as session:
        engagement = await eng_repo.create_engagement(
            session,
            name="MCP Integration Test Engagement",
            scope="10.0.0.0/8",
            client_info=None,
            owner_id=member_id,  # type: ignore[arg-type]
        )
        await session.commit()
        engagement_id = engagement.id

    # ---- build app and run the lifespan (spawns shell-exec subprocess) -----
    app = _make_mcp_app(pg_schema_factory, test_mcp_config)

    async with app.router.lifespan_context(app):
        async with AsyncClient(transport=ASGITransport(app=app), base_url="https://test") as client:
            # ---- step 3: login -----------------------------------------------
            login_resp = await client.post(
                "/api/v1/auth/login",
                json={"username": "member_user", "password": _MEMBER_PW},
            )
            assert login_resp.status_code == 200, f"Login failed: {login_resp.text}"
            assert get_settings().SESSION_COOKIE_NAME in client.cookies

            # ---- step 4: POST /api/v1/tool-runs ------------------------------
            tool_run_resp = await client.post(
                "/api/v1/tool-runs",
                json={
                    "engagement_id": str(engagement_id),
                    "server_name": "shell-exec",
                    "tool_name": "run_command",
                    "args": {"command": "echo hello"},
                },
            )
            assert tool_run_resp.status_code == 200, (
                f"tool-run failed ({tool_run_resp.status_code}): {tool_run_resp.text}"
            )

            # ---- step 5: assert response fields ------------------------------
            body = tool_run_resp.json()
            assert body["exit_code"] == 0, f"Expected exit_code 0, got {body['exit_code']}"
            assert body["stdout"] == "hello\n", (
                f"Expected stdout 'hello\\n', got {body['stdout']!r}"
            )
            assert body["server_name"] == "shell-exec"
            assert body["tool_name"] == "run_command"
            assert body["engagement_id"] == str(engagement_id)

            tool_run_id = body["tool_run_id"]

    # ---- step 6: assert tool_runs row in the DB ----------------------------
    # Query outside the lifespan context (subprocess already shut down) but the
    # DB schema is still accessible until the pg_schema_factory teardown.
    async with pg_schema_factory() as session:
        result = await session.execute(select(ToolRun).where(ToolRun.id == uuid.UUID(tool_run_id)))
        row = result.scalar_one_or_none()

    assert row is not None, f"No tool_runs row found for id={tool_run_id}"
    assert row.exit_code == 0
    assert row.stdout == "hello\n"
    assert row.engagement_id == engagement_id
    assert row.server_name == "shell-exec"
    assert row.tool_name == "run_command"
    assert row.finished_at is not None, "finished_at should be set after a completed run"
